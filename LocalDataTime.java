package Usecode.different.app;

public class LocalDataTime {

		private int date;
		private int time;

		public LocalDateTime() {
		}

		public LocalDateTime(int date) {
			super();
			this.date = date;
		}

		public LocalDateTime(int date, int time) {
			super();
			this.date = date;
			this.time = time;
		}

}
