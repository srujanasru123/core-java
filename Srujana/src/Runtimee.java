package Usecode.different.app;

public class Runtimee {
	public class Runtime {
		private String name;
		private int age;

		public Runtime() {
		}

		public Runtime(String name) {
			super();
			this.name = name;
		}

		public Runtime(String name, int age) {
			super();
			this.name = name;
			this.age = age;
		}
	}
}
